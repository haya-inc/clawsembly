#!/usr/bin/env node
import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const root = process.cwd();
const outputPath = resolve(root, "packages/capability-broker/generated-guest-mailbox-artifact.mjs");
const inputs = [
  {
    relativePath: "mailbox-protocol.mjs",
    sourcePath: resolve(root, "packages/capability-broker/mailbox-protocol.mjs")
  },
  {
    relativePath: "guest-mailbox-client.mjs",
    sourcePath: resolve(root, "packages/capability-broker/guest-mailbox-client.mjs")
  }
];

function sha256(value) {
  return `sha256-${createHash("sha256").update(value).digest("hex")}`;
}

const files = [];
const artifactHash = createHash("sha256");
for (const input of inputs) {
  const contents = await readFile(input.sourcePath, "utf8");
  artifactHash.update(input.relativePath).update("\0").update(contents).update("\0");
  files.push({
    relativePath: input.relativePath,
    bytes: Buffer.byteLength(contents),
    integrity: sha256(contents),
    contents
  });
}

const generated = `// Generated by scripts/generate-guest-mailbox-artifact.mjs. Do not edit.\n`
  + `const artifact = ${JSON.stringify({
    schemaVersion: 1,
    integrity: `sha256-${artifactHash.digest("hex")}`,
    entrypoint: "guest-mailbox-client.mjs",
    protocol: "mailbox-protocol.mjs",
    files
  }, null, 2)};\n`
  + `for (const file of artifact.files) Object.freeze(file);\n`
  + `Object.freeze(artifact.files);\n`
  + `export const GUEST_MAILBOX_ARTIFACT = Object.freeze(artifact);\n`;

if (process.argv.includes("--check")) {
  let current;
  try { current = await readFile(outputPath, "utf8"); }
  catch { current = undefined; }
  if (current !== generated) {
    process.stderr.write("Generated guest mailbox artifact is stale. Run npm run mailbox:generate.\n");
    process.exitCode = 1;
  } else {
    process.stdout.write("Generated guest mailbox artifact is current.\n");
  }
} else {
  await writeFile(outputPath, generated, "utf8");
  process.stdout.write(`Wrote ${outputPath}\n`);
}
